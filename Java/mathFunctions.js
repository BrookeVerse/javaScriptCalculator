//Function for addition
const simpleAddition = (firstNumber, secondNumber) => {
  return firstNumber + secondNumber;
};
console.log(simpleAddition(1, 2));

//function for subtract
const simpleSubtract = (a, b) => {
  return a - b;
};
console.log(simpleSubtract(3, 1));

//function for multiply
const simpleMultiply = (a, b) => {
  return a * b;
};
console.log(simpleMultiply(2, 2));

//function for divide
const simpleDivision = (a, b) => {
  return a / b;
};
console.log(simpleDivision(10, 2));

//function for percentage
const simplePercentage = (a, b) => {
  return (a / b) * 100;
};
console.log(simplePercentage(10, 100));
